package week1.day2;

public class LearnForLoop {

	public static void main(String[] args) {
		System.out.println(1);
		System.out.println(2);
		System.out.println(3);
		System.out.println(4);
		System.out.println(5);
		System.out.println("---------------");
		for (int i=0;i<=5;i++) {
			System.out.println(i);
			//i=0;0<=5;0+1=1
			//i=1;1<=5;1+1=2
			//i=2;2<=5;2+1=3
			//i=5;5<=5;5+1=6
			//i=6;6<5.....false
			//exit the loop
			
		}
		

	}

}
